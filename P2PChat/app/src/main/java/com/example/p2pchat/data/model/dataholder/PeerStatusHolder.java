package com.example.p2pchat.data.model.dataholder;

public class PeerStatusHolder {
    String name,status;
    public PeerStatusHolder(String name, String status) {
        this.name = name;
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
